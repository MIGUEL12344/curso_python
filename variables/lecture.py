# Asignar valores a variables
nombre = "María"
edad = 30
saldo_cuenta = 1500.50
es_estudiante = False

# Imprimir los valores de las variables
print("Nombre:", nombre)
print("Edad:", edad)
print("Saldo en cuenta:", saldo_cuenta)
print("¿Es estudiante?", es_estudiante)

# Realizar operaciones con variables
descuento = 200
nuevo_saldo = saldo_cuenta - descuento
print("Nuevo saldo después del descuento:", nuevo_saldo)

# Concatenar cadenas de texto con variables
mensaje = "Hola, me llamo " +nombre + " y tengo " +str(edad) + " años."
print(mensaje)